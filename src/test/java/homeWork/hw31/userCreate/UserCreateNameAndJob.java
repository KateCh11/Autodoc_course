package homeWork.hw31.userCreate;

public class UserCreateNameAndJob {

    private String name;

    private String job;

    public UserCreateNameAndJob() {
    }

    public UserCreateNameAndJob(String name, String job) {
        this.name = name;
        this.job = job;
    }

    public String getName() {
        return name;
    }

    public String getJob() {
        return job;
    }
}
